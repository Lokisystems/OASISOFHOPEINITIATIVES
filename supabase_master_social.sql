-- Dynamic Policy Creation (Checking table and name)
DO $$ 
BEGIN
    -- Comments
    IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename = 'comments' AND policyname = 'Public view comments') THEN
        CREATE POLICY "Public view comments" ON public.comments FOR SELECT USING (true);
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename = 'comments' AND policyname = 'Approved users can comment') THEN
        CREATE POLICY "Approved users can comment" ON public.comments FOR INSERT WITH CHECK (
            EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND (status = 'approved' OR role = 'admin'))
        );
    END IF;

    -- Post Media
    IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename = 'post_media' AND policyname = 'Public view post media') THEN
        CREATE POLICY "Public view post media" ON public.post_media FOR SELECT USING (true);
    END IF;

    -- Reactions
    IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename = 'post_reactions' AND policyname = 'Public view post reactions') THEN
        CREATE POLICY "Public view post reactions" ON public.post_reactions FOR SELECT USING (true);
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename = 'post_reactions' AND policyname = 'Users can react') THEN
        CREATE POLICY "Users can react" ON public.post_reactions FOR ALL USING (auth.uid() = user_id);
    END IF;

    -- Notifications
    IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename = 'notifications' AND policyname = 'Users can view own notifications') THEN
        CREATE POLICY "Users can view own notifications" ON public.notifications FOR SELECT USING (auth.uid() = user_id);
    END IF;
END $$;

-- 9. AUTOMATIC COMMENT COUNTS (TRIGGER)
CREATE OR REPLACE FUNCTION public.handle_comment_count()
RETURNS TRIGGER AS $$
BEGIN
    IF (TG_OP = 'INSERT') THEN
        UPDATE public.activities SET comments_count = comments_count + 1 WHERE id = NEW.post_id;
    ELSIF (TG_OP = 'DELETE') THEN
        UPDATE public.activities SET comments_count = comments_count - 1 WHERE id = OLD.post_id;
    END IF;
    RETURN NULL;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS on_comment_added ON public.comments;
CREATE TRIGGER on_comment_added
    AFTER INSERT OR DELETE ON public.comments
    FOR EACH ROW EXECUTE FUNCTION public.handle_comment_count();


-- 9. RPC FUNCTIONS
CREATE OR REPLACE FUNCTION public.increment_comment_count(activity_id BIGINT)
RETURNS void AS $$
BEGIN
  UPDATE public.activities
  SET comments_count = COALESCE(comments_count, 0) + 1
  WHERE id = activity_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

